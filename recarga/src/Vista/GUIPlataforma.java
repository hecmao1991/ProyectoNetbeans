    private void btnGuardarPltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPltActionPerformed
        CODIGOPLT= txtcodigoplt.getText();
        NOMBREPLT= txtnombreplt.getText();
        
        boolean insertop = cp.insertarPlataforma(CODIGOPLT, NOMBREPLT);
        
        if (insertop){
            JOptionPane.showMessageDialog(this, "Datos Almacenados!!","Guardar",JOptionPane.INFORMATION_MESSAGE);
            actualizarTablaPlataforma(CODIGOPLT);
        }else {JOptionPane.showMessageDialog(this, "Datos no Almacenados!!","Guardar",JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_btnGuardarPltActionPerformed

    private void btnEliminarPltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarPltActionPerformed
        CODIGOPLT=txtcodigoplt.getText();
        int respuesta = JOptionPane.showConfirmDialog(this, "Desea Eliminar la PLATAFORMA "+CODIGOPLT+"?");
        boolean elimino = cp.eliminarPlataforma(CODIGOPLT);
       
        
        if (respuesta == JOptionPane.YES_OPTION){
            elimino = cp.eliminarPlataforma(CODIGOPLT);
                     
        }
    }//GEN-LAST:event_btnEliminarPltActionPerformed

