    private void btnBusquedaPltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusquedaPltActionPerformed
        String Busqueda= txtBusquedaPlt.getText();
       Object dato[][]=cp.consultarPlataformaNombre(Busqueda);
       
       if ("Nombre_Plataforma".equals(jCBoxBusquedaPlt.getSelectedItem().toString())) {                
                 Object data[][] = cp.consultarPlataformaNombre(Busqueda);
                    dtmPlataforma= new DefaultTableModel(data,tablaPlataforma);
                    tblPlt.setModel(dtmPlataforma);                                         
       }else if("Codigo_Plataforma".equals(jCBoxBusquedaPlt.getSelectedItem().toString())){                
                 Object data[][] = cp.consultarPlataforma(Busqueda);
                    dtmPlataforma= new DefaultTableModel(data,tablaPlataforma);
                    tblPlt.setModel(dtmPlataforma);                    
       }else if ("Mostrar_Plataforma".equals(jCBoxBusquedaPlt.getSelectedItem().toString())){
                Object data[][] = cp.consultarPlataforma();
                    dtmPlataforma= new DefaultTableModel(data,tablaPlataforma);
                    tblPlt.setModel(dtmPlataforma);
       }else{JOptionPane.showMessageDialog(this, "Datos no Encontrados!!","Cancelar",JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_btnBusquedaPltActionPerformed

